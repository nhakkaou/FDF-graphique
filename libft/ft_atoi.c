#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	long int		res;
	int				c;
	char			*s;

	c = 1;
	i = 0;
	res = 0;
	s = ft_space(str);
	while (s[i] != '\0' && ((s[i] >= '0' && s[i] <= '9')
	|| (i == 0 && (s[0] == '-' || s[0] == '+'))))
	{
		if (s[i] == '-' || s[i] == '+')
		{
			i++;
			continue;
		}
		res = (res * 10) + (s[i] - 48);
		c = 10;
		++i;
	}
	if (s[0] == '-')
		res *= -1;
	return (res);
}
