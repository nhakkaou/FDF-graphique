char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	find;

	i = 0;
	find = c;
	while (s[i] != '\0')
	{
		if (s[i] == find)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == find)
		return ((char *)s + i);
	return (0);
}
